	  angle += degchange;

	  if (angle > 360) angle -= 360;
	  if (angle < 0)   angle += 360;

	  sprintf(data, "value = %1.5f \r\n", angle);

	  HAL_UART_Transmit(&huart3, data, 100, 0xFFFF);

